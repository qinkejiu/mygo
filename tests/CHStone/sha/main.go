// ============================================================
// 以下是 SHA-1 算法实现（无需修改）
// ============================================================
var (
	sha_info_digest   [5]INT32
	sha_info_count_lo INT32
	sha_info_count_hi INT32
	sha_info_data     [16]INT32
)

const (
	CONST1 = 0x5a827999
	CONST2 = 0x6ed9eba1
	CONST3 = 0x8f1bbcdc
	CONST4 = 0xca62c1d6
)

func f1(x, y, z INT32) INT32 { return (x & y) | ((^x) & z) }
func f2(x, y, z INT32) INT32 { return x ^ y ^ z }
func f3(x, y, z INT32) INT32 { return (x & y) | (x & z) | (y & z) }
func f4(x, y, z INT32) INT32 { return x ^ y ^ z }

func ROT32(x INT32, n uint) INT32 {
	return (x << n) | (x >> (32 - n))
}

func local_memset(s *[16]INT32, c int, n int, e int) {
	uc := INT32(c)
	m := n / 4
	idx := e
	for m > 0 && idx < 16 {
		s[idx] = uc
		idx++
		m--
	}
}

func local_memcpy(s1 *[16]INT32, s2 []BYTE, n int) {
	m := n / 4
	p2Idx := 0
	p1Idx := 0

	for m > 0 && p1Idx < 16 && p2Idx+3 < len(s2) {
		var tmp INT32
		tmp = INT32(s2[p2Idx]) << 24
		tmp |= INT32(s2[p2Idx+1]) << 16
		tmp |= INT32(s2[p2Idx+2]) << 8
		tmp |= INT32(s2[p2Idx+3])
		s1[p1Idx] = tmp
		p1Idx++
		p2Idx += 4
		m--
	}
}

func sha_transform() {
	var W [80]INT32
	var temp, A, B, C, D, E INT32

	for i := 0; i < 16; i++ {
		W[i] = sha_info_data[i]
	}
	for i := 16; i < 80; i++ {
		W[i] = ROT32(W[i-3]^W[i-8]^W[i-14]^W[i-16], 1)
	}

	A = sha_info_digest[0]
	B = sha_info_digest[1]
	C = sha_info_digest[2]
	D = sha_info_digest[3]
	E = sha_info_digest[4]

	for i := 0; i < 20; i++ {
		temp = ROT32(A, 5) + f1(B, C, D) + E + W[i] + CONST1
		E, D, C, B, A = D, C, ROT32(B, 30), A, temp
	}
	for i := 20; i < 40; i++ {
		temp = ROT32(A, 5) + f2(B, C, D) + E + W[i] + CONST2
		E, D, C, B, A = D, C, ROT32(B, 30), A, temp
	}
	for i := 40; i < 60; i++ {
		temp = ROT32(A, 5) + f3(B, C, D) + E + W[i] + CONST3
		E, D, C, B, A = D, C, ROT32(B, 30), A, temp
	}
	for i := 60; i < 80; i++ {
		temp = ROT32(A, 5) + f4(B, C, D) + E + W[i] + CONST4
		E, D, C, B, A = D, C, ROT32(B, 30), A, temp
	}

	sha_info_digest[0] += A
	sha_info_digest[1] += B
	sha_info_digest[2] += C
	sha_info_digest[3] += D
	sha_info_digest[4] += E
}

func sha_init() {
	sha_info_digest[0] = 0x67452301
	sha_info_digest[1] = 0xefcdab89
	sha_info_digest[2] = 0x98badcfe
	sha_info_digest[3] = 0x10325476
	sha_info_digest[4] = 0xc3d2e1f0
	sha_info_count_lo = 0
	sha_info_count_hi = 0
}

func sha_update(buffer []BYTE, count int) {
	if (sha_info_count_lo + (INT32(count) << 3)) < sha_info_count_lo {
		sha_info_count_hi++
	}
	sha_info_count_lo += INT32(count) << 3
	sha_info_count_hi += INT32(count) >> 29

	bufIdx := 0
	for count >= SHA_BLOCKSIZE {
		local_memcpy(&sha_info_data, buffer[bufIdx:], SHA_BLOCKSIZE)
		sha_transform()
		bufIdx += SHA_BLOCKSIZE
		count -= SHA_BLOCKSIZE
	}

	if count > 0 {
		local_memcpy(&sha_info_data, buffer[bufIdx:], count)
	}
}

func sha_final() {
	loBitCount := sha_info_count_lo
	hiBitCount := sha_info_count_hi
	count := int((loBitCount >> 3) & 0x3f)

	var dataBytes [64]BYTE
	for i := 0; i < 16; i++ {
		dataBytes[i*4] = BYTE(sha_info_data[i] >> 24)
		dataBytes[i*4+1] = BYTE(sha_info_data[i] >> 16)
		dataBytes[i*4+2] = BYTE(sha_info_data[i] >> 8)
		dataBytes[i*4+3] = BYTE(sha_info_data[i])
	}

	dataBytes[count] = 0x80
	count++

	if count > 56 {
		for i := count; i < 64; i++ {
			dataBytes[i] = 0
		}
		for i := 0; i < 16; i++ {
			sha_info_data[i] = INT32(dataBytes[i*4])<<24 |
				INT32(dataBytes[i*4+1])<<16 |
				INT32(dataBytes[i*4+2])<<8 |
				INT32(dataBytes[i*4+3])
		}
		sha_transform()
		for i := 0; i < 56; i++ {
			dataBytes[i] = 0
		}
	} else {
		for i := count; i < 56; i++ {
			dataBytes[i] = 0
		}
	}

	dataBytes[56] = BYTE(hiBitCount >> 24)
	dataBytes[57] = BYTE(hiBitCount >> 16)
	dataBytes[58] = BYTE(hiBitCount >> 8)
	dataBytes[59] = BYTE(hiBitCount)
	dataBytes[60] = BYTE(loBitCount >> 24)
	dataBytes[61] = BYTE(loBitCount >> 16)
	dataBytes[62] = BYTE(loBitCount >> 8)
	dataBytes[63] = BYTE(loBitCount)

	for i := 0; i < 16; i++ {
		sha_info_data[i] = INT32(dataBytes[i*4])<<24 |
			INT32(dataBytes[i*4+1])<<16 |
			INT32(dataBytes[i*4+2])<<8 |
			INT32(dataBytes[i*4+3])
	}

	sha_transform()
}

func sha_stream() {
	sha_init()
	for j := 0; j < VSIZE; j++ {
		i := in_i[j]
		p := indata[j][:i]
		sha_update(p, i)
	}
	sha_final()
}

